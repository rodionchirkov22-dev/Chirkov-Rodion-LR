V = 1.44           # размер дискеты в Мб
M = 100            # страниц в книге
S = 50             # строк на странице
K = 25             # символов в строке
i = 4              # байт на один символ (если используется кодировка, где символ занимает 4 байта)

# Объём одной книги в байтах
ves_knigi = K * S * M * i  # символов * байт/символ = байты

# Объём дискеты в байтах (1 Мб = 1024 Кб, 1 Кб = 1024 байт)
ves_diskety = V * 1024 * 1024  # байты

# Количество книг = объём дискеты / объём одной книги
Itog = ves_diskety / ves_knigi

print("Количество книг, помещающихся на дискету:", int(Itog))